package input;

public class Sort {
    private String rating;
    private String duration;

    public Sort() {
    }

    /**
     * getter for rating
     * @return rating
     */
    public String getRating() {
        return rating;
    }

    /**
     * setter for rating
     * @param rating
     */
    public void setRating(String rating) {
        this.rating = rating;
    }

    /**
     * getter for duration
     * @return duration
     */
    public String getDuration() {
        return duration;
    }

    /**
     * setter for duration
     * @param duration
     */
    public void setDuration(String duration) {
        this.duration = duration;
    }
}
